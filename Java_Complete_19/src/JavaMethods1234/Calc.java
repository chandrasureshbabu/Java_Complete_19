package JavaMethods1234;

public class Calc {
	
	public int calAdd(int a, int b) {
		return a+b;
	}
	
	public int calSub(int a, int b) {
		
		
		int c=a-b;
		return c;
	}
	
	public int calMulti(int c, int d)
	{
		return c*d;
	}
	
	public int calDiv(int c, int d)
	{
		return c/d;
	}
	

}
