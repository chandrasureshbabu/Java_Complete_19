package Conditional_Statemetns_Loops;

public class While_loop {

	public static void main(String[] args) {
		
		int i=1;
		while(i<=10) // if the consition is true then only it will enters loop block, for every iteration it checks condition , untill it is false
		{
			System.out.println(i);
			i++;
		}

	}

}
